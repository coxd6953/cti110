# Please enter a temperature in Celsius.
dtemp = input('Please enter a temperature in Celsius. ')
Ctemp = int(dtemp)
Ftemp = 9/5 * (Ctemp) + 32

# Display the data.
print ("\n" * 1)
print('Here is the data you entered:')
print('Celsius Temperature:', Ctemp)
print('Fahrenheit Temperature:', Ftemp)
print ("\n" * 1)
